#include <iostream>
using namespace std;
int main ()
{
	cout << "Hello World!" << endl;
	cout << "The size of the data type int is\t" << sizeof(int) << " byte(s)." << endl;
	cout << "The size of the data type char is\t" << sizeof(char) << " byte(s)." << endl;
	cout << "The size of the data type float is\t" << sizeof(float) << " byte(s)." << endl;
	cout << "The size of the data type double is\t" << sizeof(double) << " byte(s)." << endl;
	cout << "The size of the data type short is\t" << sizeof(short) << " byte(s)." << endl;
	cout << "The size of the data type boolean is\t" << sizeof(bool) << " byte(s)." << endl;
	cout << "The size of the data type long long is\t" << sizeof(long long) << " byte(s)." << endl;
	return 0;
}